public class Multiplication {
    private int val1 = 0;
    private int val2 = 0;

    public Multiplication(int val1, int val2) {
        this.val1 = val1;
        this.val2 = val2;
    }

    public int getValue() {
        return this.val1 * this.val2;
    }
}
